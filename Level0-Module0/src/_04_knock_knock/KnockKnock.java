package _04_knock_knock;

import javax.swing.JOptionPane;

public class KnockKnock {
	public static void main(String[] args) {
		JOptionPane.showMessageDialog(null, "Knock knock!");
		JOptionPane.showMessageDialog(null, "Who's there?");
		JOptionPane.showMessageDialog(null, "Etch");
		JOptionPane.showMessageDialog(null, "Etch who?");
		JOptionPane.showMessageDialog(null, "Bless you, my friend.");
	}
}
